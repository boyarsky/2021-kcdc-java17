package formatting;

public class FormatString {

    public static void main(String[] args) {

        //System.out.println("%d%%".formatted(1.2));
        System.out.println("%d%%".formatted(1));
        System.out.println("%s%%".formatted(1));
        System.out.println("%s%%".formatted(1.2));
        System.out.println("%f%%".formatted(1.2));
    }
}
